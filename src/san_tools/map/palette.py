def rgb_to_hex(rgb: tuple[int, int, int]) -> str:
    r, g, b = rgb
    return f"#{r:02x}{g:02x}{b:02x}"


def generate_palette(n=256, keep_zero_black=True):
    """
    生成 0-255 共 256 个 RGB 颜色。
    返回格式：[(R, G, B), ...]
    """
    try:
        import numpy as np
    except ImportError:
        np = None

    # 候选颜色网格，越密颜色越丰富
    values = np.linspace(0, 255, 17, dtype=np.float32)
    r, g, b = np.meshgrid(values, values, values)
    candidates = np.stack([r, g, b], axis=-1).reshape(-1, 3)

    palette = []

    # 0 通常作为背景色，固定为黑色
    if keep_zero_black:
        palette.append(np.array([0, 0, 0], dtype=np.float32))
    else:
        palette.append(np.array([255, 0, 0], dtype=np.float32))

    # 记录每个候选颜色到已选颜色集合的最小距离
    dist = np.full(len(candidates), np.inf)

    for _ in range(1, n):
        last = palette[-1]
        d = np.sum((candidates - last) ** 2, axis=1)
        dist = np.minimum(dist, d)

        # 选择离已有颜色最远的候选颜色
        idx = np.argmax(dist)
        palette.append(candidates[idx])

        # 避免重复选择
        dist[idx] = -1

    palette = np.array(palette, dtype=np.uint8)
    return [tuple(map(int, c)) for c in palette]


